#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
122b_external_tcdv_fixed_oof_stack_from_wide.py

Read wide-format external_tl_predictions.csv generated by
119_external_tcdv_scratch_vs_tl.py and perform fixed TCDV-style
no-leak outer-fold stacking.

Input wide columns:
  origin_tl_pred
  taut_tl_pred
  mean_tl_pred

Because 119 does not save per-sample cv_fold in external_tl_predictions.csv,
this script reconstructs cv_fold using the same KFold(cv_seed, cv_folds)
on each dataset sorted by stage4_index.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import numpy as np
import pandas as pd

from sklearn.model_selection import KFold
from sklearn.pipeline import make_pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import HuberRegressor, Ridge


def metric_row(y_true, y_pred):
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)

    mask = np.isfinite(y_true) & np.isfinite(y_pred)
    y_true = y_true[mask]
    y_pred = y_pred[mask]

    err = np.abs(y_true - y_pred)
    ss_res = float(np.sum((y_true - y_pred) ** 2))
    ss_tot = float(np.sum((y_true - np.mean(y_true)) ** 2)) if len(y_true) else np.nan

    return {
        "n": int(len(y_true)),
        "mae": float(np.mean(err)) if len(err) else np.nan,
        "medae": float(np.median(err)) if len(err) else np.nan,
        "rmse": float(np.sqrt(np.mean((y_true - y_pred) ** 2))) if len(err) else np.nan,
        "r2": float(1.0 - ss_res / ss_tot) if ss_tot and ss_tot > 0 else np.nan,
        "pearson": float(pd.Series(y_true).corr(pd.Series(y_pred), method="pearson")) if len(y_true) > 1 else np.nan,
        "spearman": float(pd.Series(y_true).corr(pd.Series(y_pred), method="spearman")) if len(y_true) > 1 else np.nan,
        "bias": float(np.mean(y_pred - y_true)) if len(y_true) else np.nan,
    }


def reconstruct_cv_fold(df: pd.DataFrame, cv_seed: int, cv_folds: int) -> pd.DataFrame:
    """
    Reconstruct the same outer KFold used in 119:
      sub = meta[dataset].reset_index(drop=True)
      KFold(n_splits=k, shuffle=True, random_state=cv_seed)
    The saved prediction table is already sorted by stage4_index within dataset/source_fold.
    """
    df = df.copy()
    fold_maps = []

    for ds, sub_all in df.groupby("dataset_name"):
        unique = (
            sub_all[["dataset_name", "stage4_index"]]
            .drop_duplicates()
            .sort_values("stage4_index")
            .reset_index(drop=True)
        )

        k = min(int(cv_folds), len(unique))
        cv = KFold(n_splits=k, shuffle=True, random_state=int(cv_seed))

        fold_id = np.full(len(unique), -1, dtype=int)
        for f, (_, te_idx) in enumerate(cv.split(np.zeros(len(unique)))):
            fold_id[te_idx] = f

        unique["cv_fold"] = fold_id
        fold_maps.append(unique)

    fold_map = pd.concat(fold_maps, ignore_index=True)
    out = df.merge(fold_map, on=["dataset_name", "stage4_index"], how="left")

    if out["cv_fold"].isna().any():
        bad = out[out["cv_fold"].isna()].head()
        raise RuntimeError(f"Failed to reconstruct cv_fold. Examples:\n{bad}")

    out["cv_fold"] = out["cv_fold"].astype(int)
    return out


def build_prediction_bank(df: pd.DataFrame, source_folds):
    required = [
        "dataset_name", "stage4_index", "rt", "run_key", "source_fold", "cv_fold",
        "origin_tl_pred", "taut_tl_pred",
    ]
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise KeyError(f"Missing required columns: {missing}. Available={list(df.columns)}")

    df = df[df["source_fold"].astype(int).isin([int(x) for x in source_folds])].copy()
    df["source_fold"] = df["source_fold"].astype(int)
    df["run_key"] = df["run_key"].astype(str)

    long_rows = []
    for pred_col, prefix in [
        ("origin_tl_pred", "origin"),
        ("taut_tl_pred", "taut"),
    ]:
        tmp = df[
            ["dataset_name", "stage4_index", "rt", "cv_fold", "run_key", "source_fold", pred_col]
        ].copy()
        tmp["feat_name"] = (
            prefix
            + "_"
            + tmp["run_key"].astype(str)
            + "_src"
            + tmp["source_fold"].astype(str)
        )
        tmp = tmp.rename(columns={pred_col: "pred"})
        long_rows.append(tmp)

    long_df = pd.concat(long_rows, ignore_index=True)

    index_cols = ["dataset_name", "stage4_index", "rt", "cv_fold"]

    bank = (
        long_df.pivot_table(
            index=index_cols,
            columns="feat_name",
            values="pred",
            aggfunc="mean",
        )
        .reset_index()
    )
    bank.columns.name = None

    origin_cols = sorted([c for c in bank.columns if c.startswith("origin_")])
    taut_cols = sorted([c for c in bank.columns if c.startswith("taut_")])

    if not origin_cols or not taut_cols:
        raise RuntimeError(f"Need origin and taut columns. origin={origin_cols}, taut={taut_cols}")

    # Match source-fold/run columns by suffix.
    origin_suffix = [c.replace("origin_", "") for c in origin_cols]
    taut_by_suffix = {c.replace("taut_", ""): c for c in taut_cols}

    matched_origin = []
    matched_taut = []
    for oc, suf in zip(origin_cols, origin_suffix):
        tc = taut_by_suffix.get(suf)
        if tc is not None:
            matched_origin.append(oc)
            matched_taut.append(tc)

    if len(matched_origin) == 0:
        raise RuntimeError("No matched origin/taut source-fold columns.")

    origin_mat = bank[matched_origin].astype(float).values
    taut_mat = bank[matched_taut].astype(float).values
    pair_mat = 0.5 * (origin_mat + taut_mat)

    # Fixed low-dimensional TCDV prediction-bank features.
    bank["z_origin_mean"] = np.mean(origin_mat, axis=1)
    bank["z_taut_mean"] = np.mean(taut_mat, axis=1)
    bank["z_pair_mean"] = np.mean(pair_mat, axis=1)

    bank["z_origin_std"] = np.std(origin_mat, axis=1)
    bank["z_taut_std"] = np.std(taut_mat, axis=1)
    bank["z_pair_std"] = np.std(pair_mat, axis=1)

    bank["z_delta"] = bank["z_origin_mean"] - bank["z_taut_mean"]
    bank["z_abs_delta"] = np.abs(bank["z_delta"])

    feature_cols = [
        "z_origin_mean",
        "z_taut_mean",
        "z_pair_mean",
        "z_origin_std",
        "z_taut_std",
        "z_pair_std",
        "z_delta",
        "z_abs_delta",
    ]

    if bank[feature_cols].isna().any().any():
        bad = bank[feature_cols].columns[bank[feature_cols].isna().any()].tolist()
        raise RuntimeError(f"NaN in feature cols: {bad}")

    return bank, feature_cols, matched_origin, matched_taut


def noleak_stack_one_dataset(ddf, feature_cols, stacker="huber", huber_alpha=1e-4, ridge_alpha=1.0):
    preds = []
    fold_metrics = []

    for f in sorted(ddf["cv_fold"].unique()):
        train = ddf[ddf["cv_fold"] != f].copy()
        test = ddf[ddf["cv_fold"] == f].copy()

        X_train = train[feature_cols].values.astype(float)
        y_train = train["rt"].values.astype(float)
        X_test = test[feature_cols].values.astype(float)

        if stacker == "huber":
            model = make_pipeline(
                StandardScaler(),
                HuberRegressor(epsilon=1.35, alpha=float(huber_alpha), max_iter=5000),
            )
        elif stacker == "ridge":
            model = make_pipeline(
                StandardScaler(),
                Ridge(alpha=float(ridge_alpha)),
            )
        else:
            raise ValueError(f"Unknown stacker={stacker}")

        model.fit(X_train, y_train)
        y_hat = model.predict(X_test)

        tmp = test[["dataset_name", "stage4_index", "cv_fold", "rt"] + feature_cols].copy()
        tmp["y_pred_stack"] = y_hat
        tmp["stacker"] = stacker
        preds.append(tmp)

        m = metric_row(test["rt"].values, y_hat)
        m.update({
            "dataset_name": test["dataset_name"].iloc[0],
            "cv_fold": int(f),
            "stacker": stacker,
            "n_train": int(len(train)),
            "n_test": int(len(test)),
        })
        fold_metrics.append(m)

    pred_df = pd.concat(preds, ignore_index=True)
    fold_df = pd.DataFrame(fold_metrics)
    return pred_df, fold_df


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--pred_csv", required=True)
    ap.add_argument("--out_dir", required=True)
    ap.add_argument("--cv_seed", type=int, required=True)
    ap.add_argument("--cv_folds", type=int, default=10)
    ap.add_argument("--source_folds", type=int, nargs="+", default=[0, 1, 2, 3, 4])
    ap.add_argument("--stacker", choices=["huber", "ridge"], default="huber")
    ap.add_argument("--huber_alpha", type=float, default=1e-4)
    ap.add_argument("--ridge_alpha", type=float, default=1.0)
    args = ap.parse_args()

    pred_csv = Path(args.pred_csv)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(pred_csv)
    print("=== Load wide predictions ===")
    print("pred_csv:", pred_csv)
    print("shape:", df.shape)
    print("columns:", list(df.columns))

    df = reconstruct_cv_fold(df, cv_seed=args.cv_seed, cv_folds=args.cv_folds)
    bank, feature_cols, origin_cols, taut_cols = build_prediction_bank(df, args.source_folds)

    print("\n=== Prediction bank ===")
    print("bank shape:", bank.shape)
    print("datasets:", sorted(bank["dataset_name"].unique().tolist()))
    print("source_folds:", args.source_folds)
    print("origin_cols:", origin_cols)
    print("taut_cols:", taut_cols)
    print("feature_cols:", feature_cols)

    bank.to_csv(out_dir / "tcdv_fixed_prediction_bank.csv", index=False)

    all_preds = []
    all_folds = []
    summary_rows = []

    for ds, ddf in bank.groupby("dataset_name"):
        ddf = ddf.copy()

        pred_df, fold_df = noleak_stack_one_dataset(
            ddf,
            feature_cols=feature_cols,
            stacker=args.stacker,
            huber_alpha=args.huber_alpha,
            ridge_alpha=args.ridge_alpha,
        )

        all_preds.append(pred_df)
        all_folds.append(fold_df)

        # no-leak stacked metric
        m = metric_row(pred_df["rt"].values, pred_df["y_pred_stack"].values)
        m.update({
            "dataset_name": ds,
            "method": f"tcdv_fixed_noleak_{args.stacker}_stack",
        })
        summary_rows.append(m)

        # fixed baselines from the same bank
        for col, method in [
            ("z_origin_mean", "sourcefold_origin_mean"),
            ("z_taut_mean", "sourcefold_taut_mean"),
            ("z_pair_mean", "sourcefold_origin_taut_mean"),
        ]:
            mb = metric_row(ddf["rt"].values, ddf[col].values)
            mb.update({
                "dataset_name": ds,
                "method": method,
            })
            summary_rows.append(mb)

    all_preds = pd.concat(all_preds, ignore_index=True)
    all_folds = pd.concat(all_folds, ignore_index=True)
    summary = pd.DataFrame(summary_rows)

    order = {
        f"tcdv_fixed_noleak_{args.stacker}_stack": 0,
        "sourcefold_origin_taut_mean": 1,
        "sourcefold_origin_mean": 2,
        "sourcefold_taut_mean": 3,
    }
    summary["method_order"] = summary["method"].map(order).fillna(9)
    summary = summary.sort_values(["dataset_name", "method_order"]).drop(columns=["method_order"])

    all_preds.to_csv(out_dir / "tcdv_fixed_noleak_stack_predictions.csv", index=False)
    all_folds.to_csv(out_dir / "tcdv_fixed_noleak_stack_fold_metrics.csv", index=False)
    summary.to_csv(out_dir / "tcdv_fixed_noleak_stack_summary.csv", index=False)

    meta = {
        "pred_csv": str(pred_csv),
        "cv_seed": int(args.cv_seed),
        "cv_folds": int(args.cv_folds),
        "source_folds": [int(x) for x in args.source_folds],
        "stacker": args.stacker,
        "huber_alpha": float(args.huber_alpha),
        "ridge_alpha": float(args.ridge_alpha),
        "feature_cols": feature_cols,
        "origin_cols": origin_cols,
        "taut_cols": taut_cols,
        "note": "cv_fold reconstructed from KFold using the same cv_seed/cv_folds as 119.",
    }
    with open(out_dir / "tcdv_fixed_noleak_stack_meta.json", "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2, ensure_ascii=False)

    print("\n=== Summary ===")
    print(summary.to_string(index=False))
    print("\n[SAVE]", out_dir / "tcdv_fixed_noleak_stack_summary.csv")


if __name__ == "__main__":
    main()
