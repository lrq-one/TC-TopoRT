#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
122_external_tcdv_fixed_oof_stack.py

Fixed TCDV-style external transfer stacking.

Protocol:
1. Use external OOF predictions generated by 119_external_tcdv_scratch_vs_tl.py.
2. Use all source folds uniformly. No top-k, no per-dataset source-fold selection.
3. Use origin + taut views.
4. Build a low-dimensional fixed prediction bank.
5. For each external CV fold:
   - train the stacker only on the other folds
   - predict the held-out fold
6. Report no-leak stacked external MAE.

This script does NOT train GNN models. It only stacks already generated OOF predictions.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import List, Dict, Tuple, Optional

import numpy as np
import pandas as pd

from sklearn.pipeline import make_pipeline
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import HuberRegressor, Ridge
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from scipy.stats import spearmanr


def find_col(df: pd.DataFrame, candidates: List[str], required: bool = True) -> Optional[str]:
    lower_map = {c.lower(): c for c in df.columns}
    for cand in candidates:
        if cand.lower() in lower_map:
            return lower_map[cand.lower()]
    if required:
        raise KeyError(
            f"Cannot find required column from candidates={candidates}. "
            f"Available columns={list(df.columns)}"
        )
    return None


def metric_row(y_true, y_pred) -> Dict[str, float]:
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)

    mae = mean_absolute_error(y_true, y_pred)
    rmse = mean_squared_error(y_true, y_pred, squared=False)
    medae = float(np.median(np.abs(y_true - y_pred)))

    if len(np.unique(y_true)) > 1:
        r2 = r2_score(y_true, y_pred)
        sp = spearmanr(y_true, y_pred).correlation
        if np.isnan(sp):
            sp = np.nan
    else:
        r2 = np.nan
        sp = np.nan

    return {
        "mae": float(mae),
        "medae": float(medae),
        "rmse": float(rmse),
        "r2": float(r2),
        "spearman": float(sp) if sp is not None else np.nan,
    }


def normalize_prediction_table(pred_csv: Path) -> pd.DataFrame:
    df = pd.read_csv(pred_csv)

    dataset_col = find_col(df, ["dataset_name", "dataset", "DataSet"])
    method_col = find_col(df, ["method", "view", "model_method"])
    pred_col = find_col(df, ["y_pred", "pred", "pred_rt", "prediction", "rt_pred"])
    true_col = find_col(df, ["y_true", "rt", "label", "target", "rt_true"])
    fold_col = find_col(df, ["cv_fold", "fold", "test_fold", "outer_fold"])
    source_col = find_col(df, ["source_fold", "src_fold"])
    run_col = find_col(df, ["run_key", "seed", "run"], required=False)

    # Sample id column is best if present.
    sample_col = find_col(
        df,
        ["sample_idx", "row_idx", "external_idx", "mol_idx", "idx", "data_idx", "orig_idx"],
        required=False,
    )

    keep_cols = [dataset_col, method_col, pred_col, true_col, fold_col, source_col]
    if run_col is not None:
        keep_cols.append(run_col)
    if sample_col is not None:
        keep_cols.append(sample_col)

    smiles_col = find_col(df, ["smiles", "SMILES", "canonical_smiles"], required=False)
    if smiles_col is not None:
        keep_cols.append(smiles_col)

    out = df[keep_cols].copy()

    rename = {
        dataset_col: "dataset_name",
        method_col: "method",
        pred_col: "y_pred",
        true_col: "y_true",
        fold_col: "cv_fold",
        source_col: "source_fold",
    }
    if run_col is not None:
        rename[run_col] = "run_key"
    else:
        out["run_key"] = "run"
    if sample_col is not None:
        rename[sample_col] = "sample_id"
    if smiles_col is not None:
        rename[smiles_col] = "smiles"

    out = out.rename(columns=rename)

    # Only use origin and taut base predictions. mean_tl will be reconstructed.
    out["method"] = out["method"].astype(str)
    out = out[out["method"].isin(["origin_tl", "taut_tl"])].copy()

    if len(out) == 0:
        raise ValueError(
            "No rows with method in ['origin_tl', 'taut_tl']. "
            "Please inspect external_tl_predictions.csv."
        )

    out["dataset_name"] = out["dataset_name"].astype(str)
    out["run_key"] = out["run_key"].astype(str)
    out["source_fold"] = out["source_fold"].astype(int)
    out["cv_fold"] = out["cv_fold"].astype(int)
    out["y_true"] = out["y_true"].astype(float)
    out["y_pred"] = out["y_pred"].astype(float)

    # If there is no explicit sample id, create one from cv_fold and row order.
    # This assumes 119 writes predictions in the same order across source folds/views.
    if "sample_id" not in out.columns:
        out = out.sort_values(
            ["dataset_name", "run_key", "source_fold", "method", "cv_fold"]
        ).copy()
        out["_row_in_fold"] = out.groupby(
            ["dataset_name", "run_key", "source_fold", "method", "cv_fold"]
        ).cumcount()
        out["sample_id"] = (
            out["dataset_name"].astype(str)
            + "__fold"
            + out["cv_fold"].astype(str)
            + "__row"
            + out["_row_in_fold"].astype(str)
        )

    out["sample_id"] = out["sample_id"].astype(str)

    return out


def build_feature_bank(df: pd.DataFrame, source_folds: Optional[List[int]] = None) -> pd.DataFrame:
    if source_folds is not None:
        df = df[df["source_fold"].isin(source_folds)].copy()

    if len(df) == 0:
        raise ValueError("No rows after source_fold filtering.")

    # Build feature name: origin_src0, taut_src0, ...
    df["feat_name"] = (
        df["method"].map({"origin_tl": "origin", "taut_tl": "taut"})
        + "_src"
        + df["source_fold"].astype(str)
    )

    key_cols = ["dataset_name", "run_key", "cv_fold", "sample_id"]

    # y_true consistency check
    y_check = df.groupby(key_cols)["y_true"].agg(["min", "max", "count"]).reset_index()
    bad = y_check[np.abs(y_check["max"] - y_check["min"]) > 1e-6]
    if len(bad) > 0:
        print("[WARN] y_true mismatch after grouping. Showing first rows:")
        print(bad.head(20).to_string(index=False))
        raise ValueError("y_true mismatch. sample_id alignment is unsafe.")

    y_df = df.groupby(key_cols)["y_true"].first().reset_index()

    wide = df.pivot_table(
        index=key_cols,
        columns="feat_name",
        values="y_pred",
        aggfunc="mean",
    ).reset_index()

    wide.columns.name = None
    wide = wide.merge(y_df, on=key_cols, how="left")

    origin_cols = sorted([c for c in wide.columns if c.startswith("origin_src")])
    taut_cols = sorted([c for c in wide.columns if c.startswith("taut_src")])

    if len(origin_cols) == 0 or len(taut_cols) == 0:
        raise ValueError(f"Need origin and taut cols. origin={origin_cols}, taut={taut_cols}")

    # Require matched source folds
    missing = []
    for oc in origin_cols:
        sf = oc.replace("origin_src", "")
        tc = f"taut_src{sf}"
        if tc not in taut_cols:
            missing.append(tc)
    if missing:
        raise ValueError(f"Missing matched taut cols: {missing}")

    # Fixed low-dimensional TCDV prediction bank.
    origin_mat = wide[origin_cols].astype(float).values
    taut_mat = wide[[f"taut_src{c.replace('origin_src', '')}" for c in origin_cols]].astype(float).values
    mean_pair_mat = 0.5 * (origin_mat + taut_mat)

    wide["z_origin_mean"] = np.nanmean(origin_mat, axis=1)
    wide["z_taut_mean"] = np.nanmean(taut_mat, axis=1)
    wide["z_pair_mean"] = np.nanmean(mean_pair_mat, axis=1)

    wide["z_origin_std"] = np.nanstd(origin_mat, axis=1)
    wide["z_taut_std"] = np.nanstd(taut_mat, axis=1)
    wide["z_pair_std"] = np.nanstd(mean_pair_mat, axis=1)

    wide["z_delta"] = wide["z_origin_mean"] - wide["z_taut_mean"]
    wide["z_abs_delta"] = np.abs(wide["z_delta"])

    # This is a fixed feature list for every dataset.
    feature_cols = [
        "z_origin_mean",
        "z_taut_mean",
        "z_pair_mean",
        "z_origin_std",
        "z_taut_std",
        "z_pair_std",
        "z_delta",
        "z_abs_delta",
    ]

    # Sanity check NaN
    if wide[feature_cols].isna().any().any():
        bad_cols = wide[feature_cols].columns[wide[feature_cols].isna().any()].tolist()
        raise ValueError(f"NaN in feature columns: {bad_cols}")

    return wide, feature_cols, origin_cols, taut_cols


def noleak_stack_one_dataset(
    ddf: pd.DataFrame,
    feature_cols: List[str],
    stacker: str = "huber",
    huber_epsilon: float = 1.35,
    huber_alpha: float = 1e-4,
    ridge_alpha: float = 1.0,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    preds = []
    fold_rows = []

    folds = sorted(ddf["cv_fold"].unique().tolist())

    for f in folds:
        train = ddf[ddf["cv_fold"] != f].copy()
        test = ddf[ddf["cv_fold"] == f].copy()

        X_train = train[feature_cols].values.astype(float)
        y_train = train["y_true"].values.astype(float)
        X_test = test[feature_cols].values.astype(float)

        if stacker == "huber":
            model = make_pipeline(
                StandardScaler(),
                HuberRegressor(
                    epsilon=huber_epsilon,
                    alpha=huber_alpha,
                    max_iter=1000,
                ),
            )
        elif stacker == "ridge":
            model = make_pipeline(
                StandardScaler(),
                Ridge(alpha=ridge_alpha),
            )
        else:
            raise ValueError(f"Unknown stacker: {stacker}")

        model.fit(X_train, y_train)
        y_hat = model.predict(X_test)

        tmp = test[["dataset_name", "run_key", "cv_fold", "sample_id", "y_true"] + feature_cols].copy()
        tmp["y_pred_stack"] = y_hat
        tmp["stacker"] = stacker
        preds.append(tmp)

        m = metric_row(test["y_true"].values, y_hat)
        m.update({
            "dataset_name": test["dataset_name"].iloc[0],
            "run_key": test["run_key"].iloc[0],
            "cv_fold": int(f),
            "n_train": int(len(train)),
            "n_test": int(len(test)),
            "stacker": stacker,
        })
        fold_rows.append(m)

    pred_df = pd.concat(preds, ignore_index=True)
    fold_df = pd.DataFrame(fold_rows)
    return pred_df, fold_df


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--pred_csv", type=str, required=True)
    ap.add_argument("--out_dir", type=str, required=True)
    ap.add_argument("--source_folds", type=int, nargs="*", default=None)
    ap.add_argument("--stacker", type=str, default="huber", choices=["huber", "ridge"])
    ap.add_argument("--huber_epsilon", type=float, default=1.35)
    ap.add_argument("--huber_alpha", type=float, default=1e-4)
    ap.add_argument("--ridge_alpha", type=float, default=1.0)
    args = ap.parse_args()

    pred_csv = Path(args.pred_csv)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    raw = normalize_prediction_table(pred_csv)
    bank, feature_cols, origin_cols, taut_cols = build_feature_bank(raw, args.source_folds)

    print("=== Input ===")
    print("pred_csv:", pred_csv)
    print("raw rows:", len(raw))
    print("bank rows:", len(bank))
    print("datasets:", sorted(bank["dataset_name"].unique().tolist()))
    print("source_folds:", args.source_folds)
    print("origin_cols:", origin_cols)
    print("taut_cols:", taut_cols)
    print("feature_cols:", feature_cols)

    bank.to_csv(out_dir / "tcdv_fixed_prediction_bank.csv", index=False)

    all_preds = []
    all_folds = []
    all_summary = []

    for ds, ddf in bank.groupby("dataset_name"):
        ddf = ddf.copy()

        pred_df, fold_df = noleak_stack_one_dataset(
            ddf,
            feature_cols=feature_cols,
            stacker=args.stacker,
            huber_epsilon=args.huber_epsilon,
            huber_alpha=args.huber_alpha,
            ridge_alpha=args.ridge_alpha,
        )

        all_preds.append(pred_df)
        all_folds.append(fold_df)

        # Overall no-leak stack metric
        m_stack = metric_row(pred_df["y_true"].values, pred_df["y_pred_stack"].values)
        m_stack.update({
            "dataset_name": ds,
            "method": f"tcdv_fixed_noleak_{args.stacker}_stack",
            "n": int(len(pred_df)),
        })
        all_summary.append(m_stack)

        # Fixed baselines from same prediction bank
        for col, method in [
            ("z_origin_mean", "sourcefold_origin_mean"),
            ("z_taut_mean", "sourcefold_taut_mean"),
            ("z_pair_mean", "sourcefold_origin_taut_mean"),
        ]:
            mb = metric_row(ddf["y_true"].values, ddf[col].values)
            mb.update({
                "dataset_name": ds,
                "method": method,
                "n": int(len(ddf)),
            })
            all_summary.append(mb)

    all_preds = pd.concat(all_preds, ignore_index=True)
    all_folds = pd.concat(all_folds, ignore_index=True)
    all_summary = pd.DataFrame(all_summary)

    # Put stacked result first per dataset
    all_summary["method_order"] = all_summary["method"].map(
        {f"tcdv_fixed_noleak_{args.stacker}_stack": 0}
    ).fillna(1)
    all_summary = all_summary.sort_values(["dataset_name", "method_order", "mae"]).drop(columns=["method_order"])

    all_preds.to_csv(out_dir / "tcdv_fixed_noleak_stack_predictions.csv", index=False)
    all_folds.to_csv(out_dir / "tcdv_fixed_noleak_stack_fold_metrics.csv", index=False)
    all_summary.to_csv(out_dir / "tcdv_fixed_noleak_stack_summary.csv", index=False)

    meta = {
        "pred_csv": str(pred_csv),
        "source_folds": args.source_folds,
        "stacker": args.stacker,
        "huber_epsilon": args.huber_epsilon,
        "huber_alpha": args.huber_alpha,
        "ridge_alpha": args.ridge_alpha,
        "feature_cols": feature_cols,
        "origin_cols": origin_cols,
        "taut_cols": taut_cols,
        "protocol": "fixed TCDV-style dual-view source-fold ensemble with no-leak outer-fold stacking",
    }
    with open(out_dir / "tcdv_fixed_noleak_stack_meta.json", "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2, ensure_ascii=False)

    print("\n=== Summary ===")
    print(all_summary.to_string(index=False))
    print("\n[SAVE]", out_dir / "tcdv_fixed_noleak_stack_summary.csv")


if __name__ == "__main__":
    main()
